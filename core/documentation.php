            <p id="round2" class="font-large-1">Round 2 : Guidelines</p>
            <hr>
            <p>
             The core principle of Round 2 is to make the lab/experiment documentation elaborated in a manner that it makes easy for any person (developer/domain and non domain faculty/student) to understand and develop the lab/experiment.<br>
              <h3>1. Story Outline :</h3><br>
              The proposer should first draft the story outline for the proposed outline. This could be 2 or 3 paragraphs.
              <br><br><h3>2. Story :</h3><br>
              The next step is to write a story. Story should be a brief logical description in simple English. It should talk about the overall execution of the experiment in a simplified manner. (should not include any equations/ drawings). The story should include the following points)
              <br><br><h4>1.1 Set the visual stage description.</h4><br>
                When a user lands up into a simulator, the visual stage is the first thing that user comes across. The visual stage should include all the elements to be seen by a user on an imaginary canvas.  (This should NOT include any drawings.
               <br><br><h4>1.2 Set User Objectives &amp; Goals.:</h4><br>
                The user should clearly understand the aims and objective of the experiment. ( how he will begin experiment. ?  in descriptive)
               <br><br><h4>1.3 Set the pathway activities</h4><br>
               Set the pathway activities in line with the learning objectives (LOs)  &amp; with a view to achieve the goals set in round #0 &amp; round#1.  (once he begins, how he will traverse through the entire experiment ? in descriptive)
               <br><br><h4>1.4 Set Challenges and Questions/Complexity/variations</h4><br>
                Set Challenges and Questions/Complexity/variations in questions according to User’s level, so as to invoke the learners’ interest.  (while traversing, what challenges he will face? how he has to solve and overcome ? descriptive).
                <br><br><h4>1.5 Allow pitfalls</h4><br>
                 (while traversing or while solving challenge, how will we bring a situation in front of him that he commits mistake if he is not attentive , or not following procedure , descriptive)
                <br><br><h4>1.6 Conclusion:</h4><br>
                  This may include displaying: how much time did the user take to solve the questions/ How many questions were right?/ How many hints did the learner use. Based on this the evaluation/marking to be suggested to the user. (once he is towards the completion of the experiment, what LO he is taking along with him, what will be his probable, interpretation/conclusion , descriptive)
               <br><br><h4>1.7 Equations/formulas :</h4><br>
               ( a separate sheet having equations / programs for the lab exper3ment to be shared along with the Story submissions (1) . You can mark it as numerical reference numbers within the story narration (like we cite in the research papers) and then separately share these equations/programs sheets as a reference, do not include the equations as a whole in the narration))
                 Tool can be used to integrate formula in Markdown <b><a href="http://latex.codecogs.com/eqneditor/samples/example3.php">Click Here</a> </b>
              <br><br><h3>3. Flowchart 4</h3><br>
             The lab proposer should extract logic from the story, prepare a flowchart from the story narration and write the algorithm to execute the black box.  use Google Drawings <a href="https://docs.google.com/drawings/">https://docs.google.com/drawings/</a> (send the link to your flowchart and also attach .png by exporting it
             <br><br><h3>4. Mindmap:</h3><br>
              An elaborate mind map (connecting all the points in the experiment flow ) should be prepared and submitted by the lab proposer. The mind map should be a clear and detailed document that takes into account all minute intri5acies involved in the development of virtual lab. The mindmap should be self-content and any developer across the globe should be able to code it with all those details. using only FreeMind http://freemind.sourceforge.net/wiki/index.php/Main_Page (send the .png file and also the original .mm extension project file. )
              <br><br><h3>5. Storyboard :</h3><br>
                This document should include sketching and description scene wise (duration, action, description). Software to be used for storyboarding :<a href="https://wonderunit.com/storyboarder/"> https://wonderunit.com/storyboarder/</a> (Its a FOSS tool) . tutorial on how to use it <a href="https://www.youtube.com/watch?v=LAeCEpG0KX4">https://www.youtube.com/watch?v=LAeCEpG0KX4</a>
           <br><br><h4>Sample Reference here</h4><br>
